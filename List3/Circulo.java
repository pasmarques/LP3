public class Circulo {
    public static final double  PI = 3.14;
    private int x;
    private int y;
    private int raio;
    
    public Circulo ()
    {
        this.x=5;
        this.y=4;
        this.raio=5;
    }

    public int getX()
    {
        return x;
    }

    public int getY()
    {
        return y;
    }
    
    public int getRaio()
    {
        return raio;
    }

}
