public class UnidadesMassa {

    public static double kgToGrama(int kg)
    {
        return kg*1000;
    }

    public static double kgToPound(int kg)
    {
        return kg*2.205;
    }

    public static double kgToOunce(int kg)
    {
        return kg*35.274;
    }

    public static double gramaToKg(int g)
    {
        return g*0.001;
    }

    public static double gramaToPound(int g)
    {
        return g/453.6;
    }

    public static double gramaToOunce(int g)
    {
        return g/28.35;
    }

    public static double poundToGrama(double libra)
    {
        return libra*453.6;
    }

    public static double poundToKg(double libra)
    {
        return libra/2.205;
    }

    public static double poundToOunce(double libra)
    {
        return libra*16;
    }

    public static double ounceToGrama(double ounce)
    {
        return ounce*28.35;
    }

    public static double ounceToKg(double ounce)
    {
        return ounce/35.274;
    }

    public static double ounceToPound(double ounce)
    {
        return ounce/16;
    }
    
}
